import { jsPDF } from 'jspdf';
import 'jspdf-autotable';

interface ReportData {
  patientId: string;
  date: string;
  riskLevel: 'Low' | 'Medium' | 'High';
  confidence: number;
  recommendation: string;
  lifestylePlan: string;
  previewUrl: string;
}

export const generateMedicalReport = (data: ReportData) => {
  const doc = new jsPDF();
  const pageWidth = doc.internal.pageSize.getWidth();

  // Header
  // Clean header with clinical blue
  doc.setFillColor(2, 136, 209);
  doc.rect(0, 0, pageWidth, 40, 'F');
  
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(22);
  doc.text('CATARACT GUARD AI CLINIC', 20, 25);
  doc.setFontSize(10);
  doc.text('Professional AI-Assisted Diagnostic Report', 20, 32);

  // Body Content Starts
  doc.setTextColor(44, 62, 80);
  doc.setFontSize(16);
  doc.text('Cataract Risk Assessment Report', 20, 55);
  
  doc.setFontSize(10);
  doc.text(`Patient Reference: CAT-${data.patientId.slice(-6).toUpperCase()}`, 20, 65);
  doc.text(`Report Date: ${data.date}`, 20, 70);

  // Risk Level Highlight Box
  const riskColors: Record<string, [number, number, number]> = {
    Low: [46, 125, 50],
    Medium: [237, 108, 2],
    High: [211, 47, 47]
  };
  const color = riskColors[data.riskLevel] || [44, 62, 80];
  
  // Draw risk level box
  doc.setDrawColor(color[0], color[1], color[2]);
  doc.rect(20, 80, pageWidth - 40, 30);
  
  doc.setFontSize(12);
  doc.setTextColor(color[0], color[1], color[2]);
  doc.text('RECURRENCE RISK LEVEL:', 25, 90);
  doc.setFontSize(20);
  doc.text(data.riskLevel.toUpperCase(), 25, 102);
  
  doc.setFontSize(10);
  doc.setTextColor(44, 62, 80);
  doc.text('Analysis Confidence Alignment:', pageWidth - 80, 90);
  doc.setFontSize(14);
  doc.text(`${(data.confidence * 100).toFixed(1)}%`, pageWidth - 80, 102);

  // Main Section Divider
  doc.setDrawColor(200, 200, 200);
  doc.line(20, 120, pageWidth - 20, 120);

  const sanitizeText = (str: string) => {
      if (!str) return '';
      // Remove markdown bold tags and the trailing italicized disclaimer
      let clean = str.replace(/\*\*/g, '').replace(/_[^_]*_[\s]*$/, '');
      // Remove emojis and non-standard characters, keep newlines
      clean = clean.replace(/[^\x20-\x7E\n\r]/g, ' ');
      // Clean up multiple spaces
      return clean.replace(/  +/g, ' ').trim();
  };

  // Image and Clinical Section
  // Left: Prediction Details
  doc.setFontSize(12);
  doc.setTextColor(2, 136, 209);
  doc.text('Clinical Findings:', 20, 135);
  doc.setTextColor(44, 62, 80);
  doc.setFontSize(10);
  const cleanedRec = sanitizeText(data.recommendation);
  const splitRec = doc.splitTextToSize(cleanedRec, pageWidth / 2 - 20);
  doc.text(splitRec, 20, 145);

  // Right: Analyzed Preview
  try {
     // If previewUrl is a dataURL (base64 thumbnail), it works directly
     doc.addImage(data.previewUrl, 'JPEG', pageWidth / 2 + 10, 135, 70, 70);
     doc.setDrawColor(200, 200, 200);
     doc.rect(pageWidth / 2 + 10, 135, 70, 70);
  } catch (e) {
     doc.text('[Retinal Preview Placeholder]', pageWidth / 2 + 10, 150);
  }

  // AI Lifestyle Section
  doc.setFontSize(12);
  doc.setTextColor(2, 136, 209);
  doc.text('AI Personalised Lifestyle & Dietary Plan:', 20, 220);
  
  doc.setTextColor(44, 62, 80);
  doc.setFontSize(10);
  const cleanedPlan = sanitizeText(data.lifestylePlan);
  const splitPlan = doc.splitTextToSize(cleanedPlan, pageWidth - 40);
  doc.text(splitPlan, 20, 230);

  // Footer / Disclaimer
  const footerY = 280;
  doc.setDrawColor(240, 240, 240);
  doc.setFillColor(248, 250, 252);
  doc.rect(0, footerY - 10, pageWidth, 30, 'F');
  
  doc.setFontSize(8);
  doc.setTextColor(120, 120, 120);
  doc.text('DISCLAIMER: This is an AI-assisted predictive analysis based on retinal image processing.', 20, footerY);
  doc.text('Final diagnostic decisions should be made by certified medical professionals.', 20, footerY + 5);
  doc.text('Generated by Cataract Guard Oncology & Diagnostic Suite.', pageWidth - 100, footerY + 5);

  // Final Output
  doc.save(`Cataract_Diagnosis_Report_${data.patientId.slice(-6)}.pdf`);
};
